import fs from 'node:fs';
import { defineConfig } from 'eslint/config';
import { builtinRules } from 'eslint/use-at-your-own-risk';
import tseslint from 'typescript-eslint';
import {
  typescriptConfig,
  typescriptConfigTypeChecked,
} from '../src/configs/typescript.js';

const tsEslintRulesMap = Object.fromEntries(
  Object.entries(tseslint.plugin.rules).flatMap(([ruleName, rule]) => {
    const extendsBaseRule = rule.meta?.docs?.extendsBaseRule;

    if (!extendsBaseRule) {
      return [];
    }

    const eslintRuleName =
      extendsBaseRule === true ? ruleName : extendsBaseRule;

    if (!builtinRules.has(eslintRuleName)) {
      throw new Error(
        `@typescript-eslint/${ruleName} declares missing ESLint base rule "${eslintRuleName}"`,
      );
    }

    return [[`@typescript-eslint/${ruleName}`, eslintRuleName]];
  }),
);

const typescriptRules = Object.assign(
  {},
  ...defineConfig(typescriptConfig).map((config) => config.rules ?? {}),
  ...defineConfig(typescriptConfigTypeChecked).map(
    (config) => config.rules ?? {},
  ),
);

const jsRules = Object.fromEntries(
  Object.entries(typescriptRules).flatMap(([ruleName, value]) => {
    const eslintRuleName = tsEslintRulesMap[ruleName];
    return eslintRuleName ? [[eslintRuleName, value]] : [];
  }),
);

const code = `/*
 * WARNING: This file is autogenerated using the
 * scripts/update-javascript-tseslint-rules.js script.
 * 
 * Do not edit manually.
 */

export const javascriptTSEslintRules = ${JSON.stringify(jsRules, null, 2)};
`;

fs.writeFileSync('./src/configs/javascript-tseslint-rules.js', code, 'utf8');
